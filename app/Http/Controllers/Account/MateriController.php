    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $materiVideos = Video::when(request()->q, function ($materiVideos) {
            $materiVideos = $materiVideos->where('name', 'like', '%' . request()->q . '%');
        })->latest()->paginate(10);

        $materiVideos->appends(['q' => request()->q]);

        $tahun = date('Y');
        $transactions = Transaction::with('user')
            ->where('user_id', auth()->user()->id)
            ->where('cek_ts', 1)
            ->where('tahun', $tahun)->get();
        $statusAnggota = User::where('id', auth()->user()->id)->first();
        
        return inertia('Account/Materi/Index', [
            'materiVideos' => $materiVideos,
            'transactions' => $transactions,
            'statusAnggota' => $statusAnggota,
        ]);
    }

